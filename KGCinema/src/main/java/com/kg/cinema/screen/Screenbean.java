package com.kg.cinema.screen;

public class Screenbean {
	private int s_no;
	private String s_theater;
	private String s_scrno;
	private String s_type;
	private int s_seatcnt;
	private int s_seatstyle;
	private int s_price;
	
	
	public int getS_no() {
		return s_no;
	}
	public void setS_no(int s_no) {
		this.s_no = s_no;
	}
	public String getS_theater() {
		return s_theater;
	}
	public void setS_theater(String s_theater) {
		this.s_theater = s_theater;
	}
	public String getS_scrno() {
		return s_scrno;
	}
	public void setS_scrno(String s_scrno) {
		this.s_scrno = s_scrno;
	}
	public String getS_type() {
		return s_type;
	}
	public void setS_type(String s_type) {
		this.s_type = s_type;
	}
	public int getS_seatcnt() {
		return s_seatcnt;
	}
	public void setS_seatcnt(int s_seatcnt) {
		this.s_seatcnt = s_seatcnt;
	}
	public int getS_seatstyle() {
		return s_seatstyle;
	}
	public void setS_seatstyle(int s_seatstyle) {
		this.s_seatstyle = s_seatstyle;
	}
	public int getS_price() {
		return s_price;
	}
	public void setS_price(int s_price) {
		this.s_price = s_price;
	}
}
